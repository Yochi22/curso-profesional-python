# El ciclo for es un ciclo foreach, es decir, utilizando el ciclo for, podremos iterar sobre cada uno
# de los elementos de una colección, dentro de un objeto iterable

usuarios = ['usuario1', 'usuario2', 'usuario3', 'usuario4']

# el ciclo for de comprende de: for, una variable que tomará el valor de c/u de los elementos y el objeto
# el cual queremos iterar
for usuario in usuarios:
    print(usuario)
