# Si vamos a utilizar la funcion print para imprimir un string, lo recomendable es no crear una variable para esto
# Lo recomendable es imprimir los valores directamente en consola
#La función print nos permite imprimir N cantidad de valores

nombre = 'Joselin'
apellido = 'Lozada'
#Por default la funcion print separará los valores mediante un espacio, sin embargo, podemos utilizar otros caracteres  
# Utilizando el parámetro sep =
print(nombre, apellido, 'Mendoza', sep='-')